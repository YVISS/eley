const themeIcon = '';

export default function Header() {
  const themes = ["light", "dark"];
  const [themeIcon, setThemeIcon] = useState(lightIcon); // default icon
  const [currentTheme, setCurrentTheme] = useState("light");

  // Update document attribute and localStorage
  const applyTheme = (theme) => {
    document.documentElement.setAttribute("color-scheme", theme);
    localStorage.setItem("preferred-theme", theme);
    setCurrentTheme(theme);
    setThemeIcon(theme === "light" ? lightIcon : darkIcon);
  };

  // Handle toggle
  const toggleTheme = () => {
    const nextTheme = currentTheme === "light" ? "dark" : "light";
    applyTheme(nextTheme);
  };

  // On mount: read saved theme
  useEffect(() => {
    const savedTheme = localStorage.getItem("preferred-theme") || "light";
    if (themes.includes(savedTheme)) {
      applyTheme(savedTheme);
    }
  }, []);

  return (
    <header>
      <nav>
        <ul>
          <li className="header__icons"><a href="https://github.com/YVISS" target="blank">{githubIcon}</a></li>
          <li className="header__icons"><a href="https://www.linkedin.com/in/lewee-aaron-ombac-b77077230/" target="blank">{linkedinIcon}</a></li>
          <li className="header__icons"><a href="https://www.facebook.com/roweeombac14" target="blank">{facebookIcon}</a></li>
          <li className="header__line"></li>
          <li className="header__theme">
            <a href="">
              <button className="header__switcher" onClick={toggleTheme}>
                {themeIcon}
              </button>
            </a>
          </li>
        </ul>
      </nav>
    </header>
  );
}